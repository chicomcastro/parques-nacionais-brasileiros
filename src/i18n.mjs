const LS = "parques-lang";
function detect() {
  try { const s = localStorage.getItem(LS); if (s === "pt" || s === "en") return s; } catch {}
  if (typeof navigator !== "undefined" && navigator.language && navigator.language.toLowerCase().startsWith("en")) return "en";
  return "pt";
}

let current = detect();
const listeners = new Set();

export function getLang() { return current; }
export function setLang(l) {
  if (l !== "pt" && l !== "en") return;
  current = l;
  try { localStorage.setItem(LS, l); } catch {}
  listeners.forEach(fn => fn(l));
}
export function onLangChange(fn) { listeners.add(fn); return () => listeners.delete(fn); }

export function t(key, ...args) {
  const entry = DICT[current][key] ?? DICT.pt[key] ?? key;
  return typeof entry === "function" ? entry(...args) : entry;
}
